// 1- for loop  ( Print your name & numbers from 0 through 1000)

for (let i = 1; i <= 10; i++) {
  console.log("Sanidhya", i);
}

// 2- while loop ( Print "Your Name" 100 times to the console using while loop
// Iteration will start from 10.)

let j = 10;

while (j <= 20) {
  console.log("prem", j);
  j++;
}

// 3- Do while loop ( // Print "Your Name" 400 times to the console using do-while-loop.
// Iteration should start from 20.)

let k = 20;
do {
  console.log("Deepak", k);
  k++;
} while (k <= 50);
